package com.transgrid.mib.ellipse.theos.domain;

public class WorkOrderTaskWrapperDTO {
	protected String workOrderTask;
	protected String districtCode;
	protected String workOrder;
	
	public String getWorkOrderTask() { 
		return workOrderTask;
	}
	public void setWorkOrderTask(String workOrderTask) {
		this.workOrderTask = workOrderTask;
	}
	public String getDistrictCode() {
		return districtCode;
	}
	public void setDistrictCode(String districtCode) {
		this.districtCode = districtCode;
	}
	public String getWorkOrder() {
		return workOrder;
	}
	public void setWorkOrder(String workOrder) {
		this.workOrder = workOrder;
	}
}
